/**
 * To calculate x^y given values to x and y
 */
package main.java.powerpackage;

public class PowerFinder {

    /**
     * To calculate x^y
     * uses Math.pow()
     * @param x first no
     * @param y second no
     * @return x^y
     */
    public int calculatePower(int x, int y) {
        return (int) Math.pow(x, y);
    }
}
